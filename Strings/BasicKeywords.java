
public class BasicKeywords{
public static void main(String[] args){
String string = "I am studying in SRM University";
System.out.println("The string contains SRM:"+string.contains("SRM"));
System.out.println("Length of the string is:"+string.length());
System.out.println("Substring of the string is:"+string.substring(5,14));
System.out.println("String in uppercase:"+string.toUpperCase());
System.out.println("New string:"+string.replace("SRM","VIT"));
}
}

